function setLang(lang) {
  document.querySelectorAll(".lang-btn").forEach((b) =>
    b.classList.toggle("active", b.textContent.trim() === lang.toUpperCase())
  );
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const v = translations[lang][el.getAttribute("data-i18n")];
    if (v !== undefined) el.innerHTML = v;
  });
  document.title = {
    es: "Estructuras en Movimiento · FemCoders Club × InfoJobs",
    ca: "Estructures en Moviment · FemCoders Club × InfoJobs",
    en: "Structures in Motion · FemCoders Club × InfoJobs",
  }[lang];
}

function toggleTheme() {
  const dark = document.documentElement.getAttribute("data-theme") === "dark";
  document.documentElement.setAttribute("data-theme", dark ? "light" : "dark");
  document.getElementById("themeBtn").textContent = dark ? "🌙" : "☀️";
}

// Parallax + Reveal
(function () {
  var els = [], ticking = false, sy = 0;

  function collect() {
    document.querySelectorAll("[data-parallax]").forEach(function (el) {
      els.push({ el: el, speed: parseFloat(el.dataset.parallax) || 0.1 });
    });
  }

  function update() {
    els.forEach(function (o) {
      o.el.style.transform = "translateY(" + sy * o.speed + "px)";
    });
    ticking = false;
  }

  window.addEventListener("scroll", function () {
    sy = window.scrollY;
    if (!ticking) { requestAnimationFrame(update); ticking = true; }
  }, { passive: true });

  var nav = document.getElementById("mainNav");
  window.addEventListener("scroll", function () {
    if (nav)
      nav.style.boxShadow =
        window.scrollY > 20 ? "0 2px 24px rgba(71,55,187,0.12)" : "none";
  }, { passive: true });

  var obs = new IntersectionObserver(function (entries) {
    entries.forEach(function (e) {
      if (e.isIntersecting) {
        e.target.classList.add("visible");
        obs.unobserve(e.target);
      }
    });
  }, { threshold: 0.1, rootMargin: "0px 0px -40px 0px" });

  document.addEventListener("DOMContentLoaded", function () {
    collect();
    sy = window.scrollY;
    update();
    document.querySelectorAll(".reveal,.reveal-stagger").forEach(function (el) {
      obs.observe(el);
    });
  });
})();